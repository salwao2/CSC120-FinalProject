public class tweedleTwins {

    private String name1;
    private String name2;

    public tweedleTwins(String name1, String name2) {
        this.name1 = name1;
        this.name2 = name2;
    }

    public void greet() {
        System.out.println("\nHi! We are the Tweedle twins. I am " + name1 + " and he's " + name2 + ".");
    }
}
    

